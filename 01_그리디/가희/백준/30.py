# n은 최대 10^5개의 숫자로 구성, 0으로 시작x
n = list(input()) 
n.sort(reverse=True) # 가장 큰 수 ~> 내림차순으로 정렬

sum = 0
if '0' not in n:
    print(-1)
else:
    for i in n:
        sum += int(i)
    if sum % 3 != 0:
        print(-1)
    else:
        print(''.join(n))
        
